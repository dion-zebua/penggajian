    public void loadTable(String searchValue)
    {
        KaryawanDao karyawanDao = new KaryawanDao();
        
        DefaultTableModel model = new DefaultTableModel();

        
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("G. Pokok");
        model.addColumn("NPWP");
        model.addColumn("Ter");
        model.addColumn("Jabatan");

        List<KaryawanModel> results = karyawanDao.getAllData(searchValue, false);
        for (KaryawanModel j : results) {
            model.addRow(new Object[]{
                "#" + j.getId(),
                j.getNama(),
                j.getGajiPokok(),
                j.getNpwp(),
                j.getGolonganTer(),
                j.getJabatan(),
            });
        }

        tableKaryawan.setModel(model);
    }

