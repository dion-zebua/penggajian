    private void tableKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKaryawanMouseClicked
        int barisTerpilih = tableKaryawan.getSelectedRow();

        if(barisTerpilih != -1)
        {
            Long id = Long.parseLong(
                tableKaryawan.getValueAt(barisTerpilih, 0)
                .toString()
                .replace("#", "")
            );

            BaseSetting.setDialog(new KaryawanDialog(
                new javax.swing.JFrame(), true, "Edit", id, this));
        }
    }//GEN-LAST:event_tableKaryawanMouseClicked

